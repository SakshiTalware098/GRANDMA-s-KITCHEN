                <h3>moist & easy fruit cake recipe</h3>
                <p> Prep Time-30minutes mins<br>
                    Cook Time-3hours hrs 15minutes mins<br>
                    Fruit soaking-1hour hr<br>
                    Total Time-4hours hrs 45minutes mins<br><br>
                    <b>Ingredients</b><br>
                    Fast soaked fruit (Note 1):<br>
                    30g / 1 oz raisins<br>
                    15g / 0.5 oz diced dried apricots , chopped 8 mm / 1/3"<br>
                    7.5g / 0.25 oz mixed peel , diced 5mm / 1/5<br>
                    15g / 0.5 oz glace cherries , chopped 8 mm / 1/3"<br>
                    18g / 0.6 oz dates , diced 5mm / 1/5"<br>
                    0.1 cup + 0.2 tbsp apple juice, OR 1/3 brandy + 2/3 juice (Note 2)<br><br>
                    <b>Cake:<br></b>
                    11.5g / 0.8 tbsp unsalted butter , softened (1 US stick)<br>
                    0.15 cups dark brown sugar , packed (Note 3)<br>
                    0.05 cup vegetable oil (or canola, peanut, grapeseed)<br>
                    0.3 tbsp molasses or golden syrup (Note 4)<br>
                    0.05 tsp salt<br>
                    0.05 tsp all spice<br>
                    0.05 tsp cinnamon<br>
                    0.05 tsp nutmeg<br>
                    0.3 large eggs<br>
                    0.05 tsp baking powder<br>
                    0.17 cups plain flour (all purpose flour)<br>
                    0.07 cup walnuts , chopped (optional)<br>
                    <br>
                    <b>Instructions<br></b>
                    Fast Soaked Fruit:
                    <br>1. Place dried fruit and juice/brandy in a large microwavable container. Microwave 1 1/2 minutes on high or until hot.<br>
                    2. Stir to coat all fruit in liquid. Cover then set aside for 1 hour (to plump up/soak and cool).<br>
                    Cake: 
                    <br>1. Preheat oven to 160°C / 320°F (140°C fan). Grease and line a 21 - 22 cm / 8 - 9" round cake pan with baking paper (parchment paper) (7 cm / 2.75" tall).<br>
                    2. Using an electric beater, beat butter and sugar until smooth and creamy (about 1 minute on speed 5).<br>
                    3. Add oil and molasses, beat until combined.<br>
                    4. Add salt, spices and baking powder - beat until incorporated.<br>
                    5. Add eggs, one at a time, beating until just incorporated<br>
                    6. Stir in the flour.<br>
                    7. When mostly incorporated, stir in the fruit mix (including all the extra liquid in bowl) and walnuts (if using).<br>
                    8. Pour into cake pan, cover with foil and bake for 2 1/2 hours. Remove the foil then bake for another 30 to 45 minutes, or until a skewer inserted into middle comes out clean with no batter on it (check first at 30 minutes).<br>
                    9. Remove from oven and cool for 20 minutes before transferring to cooling rack. Cool completely before serving.<br>
                    10. Cake is moist and so full flavoured, it can be eaten plain. But see below for decorating and serving ideas (traditionally served with pouring custard).<br>
                    11. Cutting: Either cut into thin wedges, or cut cake into thin strips (2cm / 0.75" or so), then cut those strips into serving size pieces.<br>
                    <b><br>Christmas Cake Decorating options:<br></b>
                    1. Traditional White Christmas Cake (pictured in post) - Marzipan and fondant, see Decorating Note.<br>
                    2. Simple - pile top with fresh cherries or other fruit, dust with icing sugar (powdered sugar). Wrap a ribbon around the cake for extra touch!<br>
                    3.Drippy white glaze - use the glaze in this Lemon Cake recipe, but leave out the lemon juice (ie make a plain sweet white glaze). Flip cake upside down for flat surface then glaze per that recipe.<br>
                    4. Serving - serve with custard for a traditional experience! Either homemade custard or store bought pouring custard (jazz it up by mixing in vanilla seed paste!).<br>
                    </p>
